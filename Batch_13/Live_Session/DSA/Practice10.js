//comparison or relational operators
let a = 7;
let b = 5;

console.log(a>b); //true -> 7>5
console.log(a<b); //false -> 7<5
console.log(a==b); //false -> 7 and 5 not equal

console.log(a<=b); //false
console.log(a>=b); //true

console.log(a!=b); //true

a=b; //a will take the value of b ( a=5 )
console.log(a);
console.log(b);