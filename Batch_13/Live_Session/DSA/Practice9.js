//Arithmetic operators
let a = 7;
let b = 5;

//+
let sum = a+b;
console.log("Addition of a and b is "+sum);

//-
let diff = a-b;
console.log("Subtraction of a and b is "+diff);

//*
let mul = a*b;
console.log("Multiplication of a and b is "+mul);

//  /
let div = a/b;
console.log("Division of a and b is "+div);

//%
let mod = a%b;
console.log("Modulus of a and b is "+mod);